# Get the pounds.
lb = float(input('Enter the pounds: '))

# Convert the pounds to kilograms.
kg = lb / 2.2046

# Display the result.
print( lb, 'pound(s) is equivalent to', format(kg, ',.2f'), 'kilogram(s)')
